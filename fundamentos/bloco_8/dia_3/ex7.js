const isNameStartWith3Dots = (name) => {
  const splitedName = name.split('.');

  for (let index = 0; index <= 2; index += 1){
    if(splitedName[index].trim().length !== 1){
      return false
    }
  }

  return true;
}

function authorWith3DotsOnName() {
  const bookWith3Dots = books.filter(book => isNameStartWith3Dots(book.author.name));

  return bookWith3Dots[0].name;
}

